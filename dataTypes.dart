void main() {
  // Integers: represents whole numbers
  int age = 25;
  print('Age: $age');

  // Doubles: represents floating point numbers
  double height = 5.11;
  print('Height: $height');

  // Strings: represents a sequence of characters
  String name = 'John Doe';
  print('Name: $name');

  // Lists: represents an ordered collection of objects
  List<String> fruits = ['apple', 'banana', 'orange'];
  print('Fruits: $fruits');

  // Maps: represents a collection of key-value pairs
  Map<String, String> person = {
    'name': 'Alice',
    'age': '30',
    'city': 'New York'
  };
  print('Person: $person');
}
